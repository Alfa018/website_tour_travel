    {
        $user_id = Auth::user()->id;
        $paket = Paket::where($request->title)->first();
        $data = DB::table('pesans')
            ->select('pesans.*', 'pakets.title')
            ->join('pakets', 'pakets.id', '=', 'pesans.id_paket')
            ->where('pesans.id_user', $user_id)
            ->get();
        
        return view('transaction', compact(['data', 'user_id', 'paket']));
    }

